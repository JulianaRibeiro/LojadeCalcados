    public void actionPerformed(ActionEvent e) {

        if (e.getSource() == buttonSalvar) {

            if ((textNome.getText().length() == 0)
                    || (textCodPro.getText().length() == 0)
                    || (textEstTotal.getText().length() == 0)
                    || (textEstMin.getText().length() == 0)
                    || (textQuant.getText().length() == 0)
                    || (textValor.getText().length() == 0)) {

                JOptionPane.showMessageDialog(null,
                        "Digite os campos obrigatorios **");
                return;
            } else {
                JOptionPane.showMessageDialog(null, "Cadastrado com Sucesso");
            }

            atual.setNome(textNome.getText());
            atual.setCod(textCodPro.getText());
            atual.setDesc(textDesc.getText());
            atual.setQtd(textQuant.getText());
            atual.setValorUn(textValor.getText());
            atual.setEstMin(textEstMin.getText());
            atual.setEstTotal(textEstTotal.getText());

            if (EhCadastro) {
                Dados.addProduto(atual);
            } else {
                try {
                    Dados.GravarProdutos();
                } catch (IOException e1) {
                    // TODO Auto-generated catch block
                    e1.printStackTrace();
                }
            }
            this.setVisible(false);
        }
        if (e.getSource() == buttonCancelar) {
            this.setVisible(false);
        }

    }

